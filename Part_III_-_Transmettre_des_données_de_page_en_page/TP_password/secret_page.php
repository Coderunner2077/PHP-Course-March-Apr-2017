    L'histoire du procédé Haber-Bosch débute avec l'invention du procédé de chimie homonyme à l'aube du xxe siècle.
    <br />
    Le procédé Haber-Bosch permet de fixer, de façon économique, le diazote atmosphérique sous forme d'ammoniac, lequel
    <br />
    permet à son tour la synthèse de différents explosifs et engrais azotés. À ce double titre, du point de vue démographique,
    <br />
    c'est probablement le plus important procédé industriel jamais mis au point durant le xxe siècle.

    Bien avant le début de la révolution industrielle, les agriculteurs qui « engraissent la terre » de différentes façons,
    <br />
    savent les bénéfices d'un apport de nutriments essentiels à la croissance des plantes. Les travaux de Justus von Liebig
    <br />
    permettent, dans les années 1840, d'identifier l'importance de l'apport en azote à cette fin. Par ailleurs, ce même
    <br />
    composé chimique pouvait déjà être transformé en acide nitrique, précurseur de la poudre à canon et de puissants explosifs
    <br />
    <pre>
        tel que le TNT et la nitroglycérine. Cependant, s'il est alors connu que l'azote constitue une part dominante de
        l'atmosphère terrestre, la chimie inorganique n'a pas encore établi de procédé pour le fixer.

    Ainsi, lorsqu'en 1909 le chimiste allemand Fritz Haber parvient à fixer l'azote atmosphérique en laboratoire, sa
        découverte présente à la fois un intérêt militaire, économique et agricole. Aussi n'est-il pas surprenant qu'en 1913,
        à peine cinq ans plus tard, une équipe de recherche de la société BASF dirigée par Carl Bosch mette au point la première
        application industrielle des travaux d'Haber : le procédé Haber-Bosch. Ce procédé servira de modèle, à la fois théorique
        et pratique, à tout un pan de la chimie industrielle moderne, la chimie à haute pression.

    La production industrielle d'ammoniac prolonge la Première Guerre mondiale en fournissant à l'Allemagne le précurseur
        de la poudre à canon et d'explosifs nécessaires à son effort de guerre, alors même qu'elle n'a plus accès aux ressources
        azotées traditionnelles, principalement exploitées en Amérique du Sud. Durant l'entre-deux-guerres, la synthèse, à
        moindre coût, d'ammoniac à partir du réservoir quasiment inépuisable que constitue l'azote atmosphérique contribue
        au développement de l'agriculture intensive et soutient la croissance démographique mondiale. Lors de la Seconde Guerre
        mondiale, les efforts d'industrialisation du procédé Haber profitent largement au procédé Bergius, lequel permet à la
        société IG Farben de réaliser la synthèse de carburant pour le compte de l'Allemagne nazie, réduisant d'autant ses
